package ex02;

public class Oper01 {
    public static void main(String[] args) {
        int n1 = 2;
        int n2 = 4;
        // 산술 연산자
        System.out.println(n1+n2);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2);

        // 결합 연산자
        System.out.println("n1+n2="+6); // 문자열 string 과 숫자열 int의 결합
    }
}
